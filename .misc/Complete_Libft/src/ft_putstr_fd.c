#include "../inc/libft.h"

/**
 * Writes the string `s` to the file descriptor `fd`.
 *
 * @param s The string to write.
 * @param fd The file descriptor on which to write.
 */
void	ft_putstr_fd(char *s, int fd)
{
	write(fd, s, ft_strlen(s));
}
