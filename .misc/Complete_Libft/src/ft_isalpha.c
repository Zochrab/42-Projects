#include "../inc/libft.h"

/**
 * Checks if the character is an alphabetical character.
 * 
 * @param c The character to check.
 * 
 * @return 1 if the character is an alphabetical character, 0 otherwise.
 */
int	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	return (0);
}
